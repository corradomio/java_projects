package jext.math.util;

public class Arithmetic {

    public static int neg(int x) { return -x; }
    public static int inv(int x) { return 1/x; }
    public static int add(int x, int y) { return x+y; }
    public static int sub(int x, int y) { return x-y; }
    public static int mul(int x, int y) { return x*y; }
    public static int div(int x, int y) { return x/y; }

    public static float neg(float x) { return -x; }
    public static float inv(float x) { return 1.f/x; }
    public static float add(float x, float y) { return x+y; }
    public static float sub(float x, float y) { return x-y; }
    public static float mul(float x, float y) { return x*y; }
    public static float div(float x, float y) { return x/y; }

}
