package jext.util;

public class Arrays {

    public static boolean[] asArray(boolean... args) {
        return args;
    }

    public static int[] asArray(int... args) {
        return args;
    }

    public static double[] asArray(double... args) {
        return args;
    }

    public static String[] asArray(String... args) {
        return args;
    }

}
